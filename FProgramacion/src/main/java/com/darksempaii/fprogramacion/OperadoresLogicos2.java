
package com.darksempaii.fprogramacion;

public class OperadoresLogicos2 {

    public static void main(String[] args) {
        //Declaracion de variables y sus valores
        int a = 20;
        int b = 10;
        //Salida a consola y sus operaciones booleanas
        System.out.println("A y B son iguales es " + (a == b));
        System.out.println("A es igual o mayor que B es " + (a >= b));
        System.out.println("A es igual o mayor que B es " + (a <= b));
        System.out.println("A es mayor que B es " + (a > b));
        System.out.println("A es munor que B es " + (a < b));
        System.out.println("A es diferente a B es " + (a != b));
    }

}
